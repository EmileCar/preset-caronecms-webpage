export type UIStyleOptions = {
    /**
     * Primary color.
     * @default 'rgb(32 32 32)'
     */
    primaryColor?: string;

    /**
     * Secondary color.
     * @default '#f0f0f0'
     */
    secondaryColor?: string;

    /**
     * Tertiary color.
     * @default 'grey'
     */
    tertiaryColor?: string;

    /**
     * Quaternary color.
     * @default 'blue'
     */
    quaternaryColor?: string;

    /**
     * Active color.
     * @default 'red'
     */
    activeColor?: string;
};